function profilebaropen()
{
    
        if( document.getElementById('profile').style.height== "0vh")
        {
            document.getElementById('profile').style.height="40.5vh" 
        }
        else {
            document.getElementById('profile').style.height="0vh" 
        }
} 

function socialbaropen()
{
    
        if( document.getElementById('social').style.width== "0vw")
        {
            document.getElementById('social').style.width="5vw" 
        }
        else {
            document.getElementById('social').style.width="0vw" 
        }
} 